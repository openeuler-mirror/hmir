import { invoke } from "@tauri-apps/api/tauri";

//调用函数前的时间限制
const timeout = 0
//登录
export function cmd_login(data: any) {
  return invoke('cmd_login', data)
}

//登出
export function cmd_logout(data: any) {
  return invoke('cmd_logout', data)
}

//连接控制台
export function cmd_ttyd_start(data: any) {
  return invoke('cmd_ttyd_start', data)
}

//断开控制台
export function cmd_ttyd_stop(data: any) {
  return invoke('cmd_ttyd_stop', data)
}

//退出系统
export function cmd_service_all() {
  return invoke('cmd_service_all')
}

//退出系统
export function cmd_quit() {
  return invoke('cmd_ttyd_stop')
}

//系统服务的启动
export function cmd_service_enabled(data: any) {
  return new Promise<void>((resolve, reject) => {
    setTimeout(() => {
      invoke('cmd_service_enabled', data).then(
        res => {
          resolve(res as any)
        }
      )
    }, timeout)
  })
}

//系统服务的禁用
export function cmd_service_disabled(data: any) {
  return new Promise<void>((resolve, reject) => {
    setTimeout(() => {
      invoke('cmd_service_disabled', data).then(
        res => {
          resolve(res as any)
        }
      )
    }, timeout)
  })
}

